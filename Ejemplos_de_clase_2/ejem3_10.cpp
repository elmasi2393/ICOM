#include "icom_helpers.h"

int main (void)
{
    int a = 100;
    int b = 2;
    int c = 25, d = 4;

    cout << "a: " << a << "b: " << b << "c: " << c << "d: " << d << '\n';

    cout << "a - b = " << a - b << '\n';
    cout << "b * c = " << b * c << '\n';
    cout << "a / c = " << a / c << '\n';
    cout << "a + b * c = " << a + b *  c << '\n';
    cout << "c / d = " << c / d << '\n';

    return 0;
}
